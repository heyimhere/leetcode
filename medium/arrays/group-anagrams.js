const groupAnagrams = function(str) {
	const hashmap = new Map();
	const result = [];

	for(let word of str) {
		const sortedStr = word.split("").sort().join("");
		if(hashmap.has(sortedStr)) {
			const key = hashmap.get(sortedStr);
			result[key].push(word);
		} else {
			hashmap.set(sortedStr, result.length);
			result.push([word]);
		}
	}
	return result;
}

